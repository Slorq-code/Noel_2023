<p>VIII. Tratamiento y Finalidad</p>
<p>El Tratamiento que llevarán a cabo los Responsables de las bases de datos se realizará
conforme a lo establecido en la Ley 1581 de 2012, el Decreto 1377 de 2013 y las normas
que lo adicionen o modifiquen, así como con lo establecido en la presente Política.</p>
<p>Los datos personales recolectados únicamente serán utilizados con las finalidades
establecidas en los respectivos avisos de privacidad o autorizaciones de tratamiento de
datos que se pongan a disposición de los Titulares con el fin de que autoricen el
Tratamiento de los mismos.</p>
<p>No obstante lo anterior, las bases de datos también podrán ser utilizadas para las
siguientes finalidades:</p>
<ul>
  <li><p>Efectuar las gestiones pertinentes para el desarrollo del objeto social de la
compañía en lo que tiene que ver con el cumplimiento del objeto del contrato
celebrado con el Titular de la información.</p></li>
  <li><p>Realizar invitaciones a eventos y ofrecer nuevos productos o servicios.</p></li>
  <li><p>Gestionar trámites de solicitudes, quejas o reclamos</p></li>\
</ul>
<p>IX. Tratamiento de Datos Sensibles</p>
<p>En caso de que los Responsables realicen el Tratamiento de Datos Sensibles, se garantiza
que se obtendrá la autorización para el tratamiento de dichos datos de manera previa y
expresa, cumpliendo con las siguientes obligaciones:</p>
<ul>
  <li><p>Se informará al Titular de los datos que por tratarse de Datos Sensibles no está
obligado a autorizar su Tratamiento.</p></li>
  <li><p>Se le informará de forma previa y expresa al Titular cuáles son los datos sensibles
y cuál será la finalidad del Tratamiento que se les dará.</p></li>
  <li><p>No se condicionará ninguna actividad a que el Titular suministre Datos Sensibles.</p></li>
</ul>
